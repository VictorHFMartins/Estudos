// Escrever um programa para exibir os números pares de 1 até 50 na tela.

package PSC.lista_de_exercicios.lista_while;

public class ex_2 {
    public static void main(String[] args) {
        int cont = 0;
        while (cont < 50) {
            cont++;
            System.out.print(cont + ", ");
        }
    }
}
